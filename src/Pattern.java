class Pattern {
    // Main method takes array of strings as an input
    public static void main(String args[]) {


        for (int j = 1 ;j<=3;j++)
        {
            for (int i = 1; i <= j; i++) {
                System.out.print('*');
            }
            System.out.println();

        }

    }
}
