//Print Number 1 to 5 using for loop using this condition (i<10)
public class QapitolExample7 {
    public static void main(String[] args) {
        for (int i =1; i<10; i++)
        {
            if(i<=5)
            {
                System.out.println(i);
            }
            else {
                break;
            }
        }

    }
}
